// Нормализация единиц измерения (PLAN 3.4).
// Единица из речи ("мешок", "куб", "тонна") приводится к канону прайса.
// Словарь минимальный, расширяется по реальным данным заказчика.

const UNIT_SYNONYMS: Record<string, string> = {
  мешок: "шт",
  мешка: "шт",
  мешков: "шт",
  штука: "шт",
  штуки: "шт",
  штук: "шт",
  шт: "шт",
  куб: "м3",
  куба: "м3",
  кубов: "м3",
  кубометр: "м3",
  "м3": "м3",
  "м³": "м3",
  тонна: "т",
  тонн: "т",
  тонны: "т",
  т: "т",
  килограмм: "кг",
  кг: "кг",
  метр: "м",
  метра: "м",
  метров: "м",
  м: "м",
  "м2": "м2",
  "м²": "м2",
  квадрат: "м2",
  литр: "л",
  литра: "л",
  литров: "л",
  л: "л",
};

export function normalizeUnit(raw: string): string {
  const key = raw.trim().toLowerCase();
  return UNIT_SYNONYMS[key] ?? key;
}

/** Известна ли словоформа как единица измерения. */
export function isKnownUnit(raw: string): boolean {
  return raw.trim().toLowerCase() in UNIT_SYNONYMS;
}
